// Arrays e Coleções

// Acessar elementos específicos de um array
let lista01 = [1, 2, 3, 4, 5]
let lista02 = [1, 2, 3, 4, 5]
console.log(lista01, lista02)
// Modificar cada um dos elementos de um array e imprimi-los no console.
lista01.join(" - ")
console.log(lista01, lista02)
// Extrair elementos de um array.
lista01.shift(1)
lista02.pop(5)
console.log(lista01, lista02)
// Adicionar elementos a um array.
lista01.unshift(0)
lista02.unshift(0)
console.log(lista01, lista02)

console.log(lista01 == lista02 ? "iguais" : "diferentes")

